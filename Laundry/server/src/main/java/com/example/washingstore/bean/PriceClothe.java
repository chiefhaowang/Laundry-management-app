package com.example.washingstore.bean;

public class PriceClothe {
    private String name;
    private int shorter;
    private int mid;
    private int longer;
    private int suplong;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getShorter() {
        return shorter;
    }

    public void setShorter(int shorter) {
        this.shorter = shorter;
    }

    public int getMid() {
        return mid;
    }

    public void setMid(int mid) {
        this.mid = mid;
    }

    public int getLonger() {
        return longer;
    }

    public void setLonger(int longer) {
        this.longer = longer;
    }

    public int getSuplong() {
        return suplong;
    }

    public void setSuplong(int suplong) {
        this.suplong = suplong;
    }
}
